-- Narrow feature surface for live / near-real-time scoring (downstream ML daemon).
-- Omits final scrap labels and rollup scrap fields to avoid training-serving skew when
-- the model is only allowed pre-outcome process features. Full labels stay on v_ml_heat_dataset_v1.
-- Companion to 067_ml_heat_dataset.sql (Spuncast-Operations / 097_ml_heat_early_score_view.sql).

