    alloy_grade,
    grade_name,
    chemistry_grade,
    pour_date,
    shift,
    job_number,
    stock_code,
    spec_code,
    furn_no,
    die_no,
    melter,
    cmop,
    analysis_equipment_code,
    operator_id,
    heat_start_ts,
    heat_end_ts,
    heat_created_at,
    pour_entered_at,
    heat_treat_start_ts,
    heat_treat_end_ts,
    chem_last_tested_at,
    tap_temp_missing,
    pour_temp_missing,
    die_temp_missing,
    die_rpm_missing,
    spec_code_missing,
    stock_code_missing,
    die_no_missing,
    tap_minus_pour_temp,
    tap_minus_die_temp,
    pour_minus_die_temp,
    chem_not_ok_flag,
    has_any_chem_alert,
    heat_treat_operator_id,
    heat_treat_temp_delta,
    lot_count,
