    pour_deviation_from_instruction_pct,
    die_deviation_from_instruction_pct,
    rpm_deviation_from_instruction_pct
FROM v_ml_heat_dataset_v1;

COMMENT ON VIEW v_ml_heat_early_score_v1 IS
    'Pre-outcome feature slice for live scoring; includes cr_pct (latest Cr reading) and '
    'instruction deviation columns (tap/pour/die/rpm_deviation_from_instruction_pct, instruction_source) '
    'from correctly-scoped process_control_profiles. '
    'Excludes scrap_flag, reason_code, defect_type, scrap_estimated_cost, analysis_date, '
    'has_scrap, latest_scrap_ts, reason_code_bucket, and all post-outcome scrap diagnostics.';
